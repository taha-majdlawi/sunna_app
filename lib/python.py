def normalize_to_english(text):
    arabic_nums = '٠١٢٣٤٥٦٧٨٩'
    english_nums = '0123456789'
    trans = str.maketrans(arabic_nums, english_nums)
    return text.translate(trans)

# 1. تحليل القائمة وبناء قاموس العناوين
titles_map = {}
lines = raw_data_input.strip().split('\n')

for line in lines:
    clean_line = normalize_to_english(line.strip())
    # استخراج الرقم الموجود بين القوسين (رقم الحلقة)
    match = re.search(r'\( ?(\d+) ?\)', clean_line)
    if match:
        num = match.group(1).zfill(3)
        # استخراج العنوان النصي قبل النقاط أو الأرقام
        title_part = re.sub(r'^\d+\.\s*', '', clean_line) # حذف الرقم المتسلسل في البداية
        title_part = title_part.split('..')[0].split('....')[0].split('السيرة النبوية')[0].strip()
        
        # إذا كان العنوان فارغاً (مثل الحلقات الوسطى)، نضع اسماً افتراضياً
        if not title_part or title_part.isdigit():
            title_part = f"السيرة النبوية - الحلقة {int(num)}"
            
        titles_map[num] = title_part

# 2. فحص المجلد وربط الملفات بالعناوين
os.makedirs(os.path.dirname(json_output_path), exist_ok=True)
final_list = []

# جلب الملفات وترتيبها
if os.path.exists(project_assets_dir):
    files = sorted([f for f in os.listdir(project_assets_dir) if f.endswith(".txt")])
    
    for f_name in files:
        num_str = f_name.replace(".txt", "")
        # جلب العنوان الحقيقي أو وضع عنوان افتراضي إذا لم يوجد في القائمة
        display_title = titles_map.get(num_str, f"السيرة النبوية ({int(num_str)})")
        
        final_list.append({
            "number": num_str,
            "title": num_str,
            "path": f"assets/transcripts/{f_name}",
            "youtube_title": display_title,
            "youtube_url": f"https://www.youtube.com/results?search_query=السيرة+النبوية+علاء+حامد+حلقة+{int(num_str)}"
        })

# 3. حفظ ملف الـ JSON
with open(json_output_path, "w", encoding="utf-8") as f:
    json.dump(final_list, f, ensure_ascii=False, indent=4)

